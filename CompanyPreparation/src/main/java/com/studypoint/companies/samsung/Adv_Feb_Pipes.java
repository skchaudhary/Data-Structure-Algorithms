package com.studypoint.companies.samsung;

import java.util.Scanner;

public class Adv_Feb_Pipes {
	static boolean[][] visited;
	static int[][] graph;
	static int pipes,m,n;
	private static Scanner sc;
	
	public static void main(String[] args) {
		sc = new Scanner(System.in);
		int t=sc.nextInt();
		while(t-->0){
			m=sc.nextInt();
			n=sc.nextInt();
			int x=sc.nextInt();
			int y=sc.nextInt();
			int length=sc.nextInt();
			visited=new boolean[m][n];
			graph=new int[m][n];
			
			for(int i=0;i<m;i++){
				for(int j=0;j<n;j++){
					graph[i][j]=sc.nextInt();
				}
			}
			pipes=0;
			solve(x,y,x,y,length);
			System.out.println("# "+pipes);
		}
	}
	private static void solve(int i, int j, int x, int y, int length) {
		// TODO Auto-generated method stub
		if(length<=0)return;
		if(x<0 || x>=m || y<0 || y>=n)return;
		if(graph[x][y]==0)return;
		if(!isAllRight(i,j,x,y))return;
		
		if(!visited[x][y]){
			pipes++;
			visited[x][y]=true;
		}
		
		if(graph[x][y]==1){
			solve(x,y,x-1,y,length-1);
			solve(x,y,x+1,y,length-1);
			solve(x,y,x,y-1,length-1);
			solve(x,y,x,y+1,length-1);
		}
		else if(graph[x][y]==2){
			solve(x,y,x-1,y,length-1);
			solve(x,y,x+1,y,length-1);
		}
		else if(graph[x][y]==3){
			solve(x,y,x,y-1,length-1);
			solve(x,y,x,y+1,length-1);
		}
		else if(graph[x][y]==4){
			solve(x,y,x-1,y,length-1);
			solve(x,y,x,y+1,length-1);
		}
		else if(graph[x][y]==5){
			solve(x,y,x+1,y,length-1);
			solve(x,y,x,y+1,length-1);
		}
		else if(graph[x][y]==6){
			solve(x,y,x+1,y,length-1);
			solve(x,y,x,y-1,length-1);
		}
		else if(graph[x][y]==7){
			solve(x,y,x-1,y,length-1);
			solve(x,y,x,y-1,length-1);
		}
	}
	private static boolean isAllRight(int i, int j, int x, int y) {
		// TODO Auto-generated method stub
		if(i==x && j==y)return true;
		if(graph[x][y]==1){
				return true;
		}
		else if(graph[x][y]==2){
			if((i==x+1 && j==y)||(i==x-1 && j==y))
				return true;
		}
		else if(graph[x][y]==3){
			if((i==x && j==y+1)||(i==x && j==y-1))
				return true;
		}
		else if(graph[x][y]==4){
			if((i==x && j==y+1)||(i==x-1 && j==y))
				return true;
		}
		else if(graph[x][y]==5){
			if((i==x && j==y+1)||(i==x+1 && j==y))
				return true;
		}
		else if(graph[x][y]==6){
			if((i==x && j==y-1)||(i==x+1 && j==y))
				return true;
		}
		else if(graph[x][y]==7){
			if((i==x && j==y-1)||(i==x-1 && j==y))
				return true;
		}
		return false;
	}
}

